package Mathematik;

public class Grundrechenarten {

	public static void main(String[] args) {
		int zahl1 = 5;
		int zahl2 = 8;
		
		int addition = zahl1 + zahl2;
		int subtraktion = zahl1 - zahl2;
		int division = zahl1 / zahl2;
		int multiplikation = zahl1 * zahl2;
		
		System.out.println("Addition: "+addition +" Subtraktion: "+subtraktion +" Division: " +division +" Multiplikation: " +multiplikation);
		

	}

}
